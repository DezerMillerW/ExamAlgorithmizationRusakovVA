a = int(input("Введите кол-во чисел: "))
c = 0
pl = float(0.5)
for i in range(a):
    b = int(input("Введите число: "))
    c+=b
print("Среднее значение: ",c/a,"Округленное в меньшую сторону: ", c//a)
if (c/a) - (c//a) < pl:
    print('Округление по правилам: ',c//a)
else:
    print("Округление по правилам: ",c//a+1)